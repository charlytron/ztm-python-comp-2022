# Concatenation is the process of combining two or more strings together.

'hello' + ' Dolly'  # => 'hello Dolly'
'hello' + ' ' + 'Dolly'  # => 'hello Dolly'

# You can also use the + operator with numbers.

print('Moron' + 5) # => 'hello5' doesn't result here.
# can't concatenate a string and a number.

