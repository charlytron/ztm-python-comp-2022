basket = [1, 2, 'u', 'c', 3, 4, 5]
print(basket.index('u')) # gives us the index of the item in the list (2)

print(basket.index('u', 0, 3)) # gives us the index of the item in the list (2)
# starting from index 0 and ending at index 3 (3)


